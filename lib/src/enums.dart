// =============================================================================
// 分片下载状态枚举
// =============================================================================

/// 分片的下载状态
enum SegmentStatus {
  /// 等待下载
  pending,

  /// 正在下载
  downloading,

  /// 下载完成
  completed,

  /// 下载失败
  failed,
}

/// 媒体类型枚举
enum MediaType {
  /// 未知
  unknown,

  /// 视频
  video,

  /// 音频
  audio,

  /// MP4 家族（需要特殊处理）
  mp4Family,
}
